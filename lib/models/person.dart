abstract class Person {
  final int id;
  String fullname;
  String gender;
  String birthday;
  String email;
  String phone;
  String address;
  String description;
  String status;
  int roleId;
  String avatarImageLink;

  Person(
      this.id,
      this.fullname,
      this.gender,
      this.birthday,
      this.email,
      this.phone,
      this.address,
      this.status,
      this.roleId,
      this.description,
      this.avatarImageLink);

  
}
